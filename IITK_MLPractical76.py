#Feature Selection:4 Ways

    #1.Univariate Feature Section(Based on concept of chi squared method)
    #2.Recursive Feature Elimination.(RFE)(Based on p-value)
    #3.Principle Component Analysis(PCA).(Dimension Reduction Technique)
    #4.Feature Selection by their Importance.(Based on IG Index)


# Three benefits of performing feature selection before modeling your data are:
    # 1) Reduces Overfitting:
        # Less redundant data means less opportunity to make
        # Decisions based on noise.(Noise:Useless Data)

    # 2) Improves Accuracy:
        # Less misleading data means modeling accuracy improves.

    # 3) Reduces Training Time:
        # Less data means that algorithms train faster.
